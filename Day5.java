public class Day5 {
    public static void main(String[] args) {
        
        // Deklarasi variabel dengan berbagai tipe data bilangan bulat
        byte angkaByte = 100;
        short angkaShort = 30000;
        int angkaInt = 1000000;
        long angkaLong = 10000000000L; // harus pakai L di akhir
        
        // Menampilkan hasil
        System.out.println("Nilai byte  : " + angkaByte);
        System.out.println("Nilai short : " + angkaShort);
        System.out.println("Nilai int   : " + angkaInt);
        System.out.println("Nilai long  : " + angkaLong);
    }
}
